
public class ArithOp {

	public static void main(String[] args) {

		int a = ((5 - 2) - 1) * 3 * 6;
		System.out.println(a);
		
		int num1 = 7;
		int num2 = 3;
		System.out.println(num1 / num2);
		System.out.println(num1 / (double) num2);
		System.out.println(num1 % num2);
		
	}

}
